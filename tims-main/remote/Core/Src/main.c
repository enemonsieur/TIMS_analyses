uint16_t adc_sample(ADC_HandleTypeDef *handle, uint32_t channel) {
  ADC_ChannelConfTypeDef sConfig = {0};
  sConfig.Channel = channel;
  sConfig.SamplingTime = ADC_SAMPLETIME_3CYCLES;
  sConfig.Rank = 1;
  HAL_ADC_ConfigChannel(handle, &sConfig);
  HAL_ADC_Start(handle);

  while (HAL_ADC_PollForConversion(handle, 1000000) != HAL_OK)
    ;
  return (uint16_t)HAL_ADC_GetValue(handle);
}

void Sample_Timer_Interrupt_Handler() {
  spi_tx_buffer[0] = adc_sample(&AIN0_HADC, AIN0_CH)<<3;
  spi_tx_buffer[1] = adc_sample(&AIN1_HADC, AIN1_CH)<<3;
  spi_tx_buffer[2] = adc_sample(&AIN2_HADC, AIN2_CH)<<3;
  spi_tx_buffer[3] = adc_sample(&AIN3_HADC, AIN3_CH)<<3;
  spi_tx_buffer[4] = adc_sample(&AIN4_HADC, AIN4_CH)<<3;
  spi_tx_buffer[5] = adc_sample(&AIN5_HADC, AIN5_CH)<<3;

  HAL_GPIO_WritePin(SPI1_CS_GPIO_Port, SPI1_CS_Pin, 0);
  HAL_SPI_TransmitReceive(&hspi1, (uint8_t *)spi_tx_buffer, (uint8_t *)spi_rx_buffer, 8, 10000);
  HAL_GPIO_WritePin(SPI1_CS_GPIO_Port, SPI1_CS_Pin, 1);

  HAL_DAC_SetValue(&hdac, DAC_CHANNEL_1, DAC_ALIGN_12B_R, spi_rx_buffer[0]>>3);
  HAL_DAC_SetValue(&hdac, DAC_CHANNEL_2, DAC_ALIGN_12B_R, spi_rx_buffer[1]>>3);
}

